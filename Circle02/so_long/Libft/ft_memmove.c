#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned int	i;

	if (dest < src)
	{
		i = 0;
		while (i < n)
		{
			*(unsigned char *)(dest + i) = *(unsigned char *)(src + i);
			i++;
		}
	}
	else if (dest > src)
	{
		i = 1;
		while (i <= n)
		{
			*(unsigned char *)(dest + n - i) = *(unsigned char *)(src + n - i);
			i++;
		}
	}
	return (dest);
}
